#include "ft_complex.h"

t_complex	c_mult(t_complex z1, t_complex z2)
{
	t_complex	z;

	z.x = z1.x * z2.x - z1.y * z2.y;
	z.y = z1.x * z2.y + z1.y * z1.x;
	return (z);
}
